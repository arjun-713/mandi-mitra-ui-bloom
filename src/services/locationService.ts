export class LocationService {
  static getStates(): string[] {
    const states = [...new Set(locationData.map(item => item.state))];
    return states.sort();
  }

  static getDistrictsByState(state: string): string[] {
    const districts = locationData
      .filter(item => item.state === state)
      .map(item => item.district);
    return [...new Set(districts)].sort();
  }

  static getMarketsByDistrict(state: string, district: string): string[] {
    const markets = locationData
      .filter(item => item.state === state && item.district === district)
      .map(item => item.market);
    return markets.sort();
  }

  static getAllMarkets(): string[] {
    return [...new Set(locationData.map(item => item.market))].sort();
  }

  static getCrops(): string[] {
    return cropsList.sort();
  }
}
